-- Criando Tabela de Usuários
CREATE TABLE IF NOT EXISTS users (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(255) NOT NULL,
cpf VARCHAR(50) NOT NULL,
email VARCHAR(255) NOT NULL,
password_hash VARCHAR(255) NOT NULL,
created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT now()
);
-->===================================================================================================
CREATE UNIQUE INDEX IF NOT EXISTS index_cpf ON users(cpf);
CREATE UNIQUE INDEX IF NOT EXISTS index_email ON users(email);
-->===================================================================================================
-- Criando Tabela Contas
CREATE TABLE IF NOT EXISTS accounts (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
balance NUMERIC(19,2) NOT NULL DEFAULT 0,
user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE
);
-->===================================================================================================
-- Criando Tabela de Cobranças
CREATE TABLE IF NOT EXISTS charges (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
origin_id BIGINT NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
destination_id BIGINT NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
amount NUMERIC(19,2) NOT NULL,
description TEXT,
status VARCHAR(20) NOT NULL DEFAULT 'PENDING',
payment_method VARCHAR(20),
created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
CONSTRAINT chk_charge_status CHECK (status IN ('PENDING','PAID','CANCELED')),
CONSTRAINT chk_payment_method CHECK (payment_method IN ('BALANCE','CARD'))
);
-->===================================================================================================
CREATE INDEX IF NOT EXISTS index_origin_id ON charges(origin_id);
CREATE INDEX IF NOT EXISTS index_destination_id ON charges(destination_id);